package StepDefinitions;

public class Book {

	private String book;
	private String author;
	
	public Book()
	{
		
	}
	
	public void setBook(String book) {
		this.book = book;
	}

	
	public String getBook() {
		return book;
	}
	
	public void setAuthor(String author) {
		this.author = author;
	}

	public String getAuthor() {
		return author;
	}
	
}
